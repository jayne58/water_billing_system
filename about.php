<h4>Our History</h4>
<p>Nairobi City Water and Sewerage Company (NCWSC) was incorporated in December 2003 under the Companies Act cap 486.It
is a wholly-owned subsidiary of Nairobi City County with the main responsibility of providing and managing water and 
sewerage services in Nairobi, while the asset holding body, Athi Water Works Development Agency (AWWDA) is charged with
the responsibility of developing key water and sewerage infrastructure. 
The Company is mandated to provide clean water and sewerage services to the residents of Nairobi City County, 
in a financially sustainable manner and within Government regulations.<br><br>
The City has an estimated population of 4.39 million by 2019 which is projected to grow to 5.9 million by 2022 
according to Nairobi County Integrated Development Plan (CIDP) 2018-2022. As the mandated water and sewerage service 
provider in Nairobi City County, NCWSC has continued to enhance service provision by aligning itself to the Constitution 
of Kenya 2010; National Water Master Plan 2030 developed in 2013 by the Government of Kenya, identifying key 
multi-sectoral initiatives and projects to ensure sustainable availability and management of water and sanitation for all
; Kenyan economic blueprint, Vision 2030’s under MTP III; Nairobi Integrated Urban Development Masterplan (NIUPLAN); 
the County’s Strategic Plan (2015-2025); Nairobi County Integrated Development Plan for (2018-2022); and Agenda 2030, 
the Sustainable Development Goals under agenda six.
The Nairobi City Water and Sewerage Company is committed to ensuring that all stakeholders receive water regularly and efficiently and that the water reaching the customers is of highest quality.</p>
